#include "libft.h"

void	ft_putnbr(int nb)
{
	char	*chain;
	int		i;

	chain = "-2147483648";
	i = 0;
	if (nb == -2147483648)
	{
		while (i < 11)
		{
			ft_putchar(chain[i++]);
		}
		return ;
	}
	if (nb < 0)
	{
		nb *= -1;
		ft_putchar('-');
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	ft_putchar((nb % 10) + 48);
}
